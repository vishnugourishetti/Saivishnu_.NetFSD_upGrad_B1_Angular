class MathUtils {
    static add(a, b) {
        return a + b;
    }

    static subtract(a, b) {
        return a - b;
    }

    static multiply(a, b) {
        return a * b;
    }
}

console.log(MathUtils.add(10, 20));
console.log(MathUtils.subtract(30, 5));
console.log(MathUtils.multiply(4, 6));