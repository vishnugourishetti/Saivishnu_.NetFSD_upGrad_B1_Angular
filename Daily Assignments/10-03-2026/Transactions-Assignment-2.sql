CREATE PROCEDURE sp_RecordExamMarks
@StudentID INT,
@ExamID INT,
@Marks INT
AS
BEGIN
BEGIN TRANSACTION

BEGIN TRY

INSERT INTO Marks(StudentID,ExamID,MarksObtained)
VALUES(@StudentID,@ExamID,@Marks)

UPDATE Exams
SET ExamType = ExamType
WHERE ExamID = @ExamID

COMMIT TRANSACTION

END TRY
BEGIN CATCH

ROLLBACK TRANSACTION
PRINT ERROR_MESSAGE()

END CATCH
END;